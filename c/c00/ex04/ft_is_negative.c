#include <unistd.h>

void	ft_is_negative(int n)
{
	char	sinal;

	if (n < 0)
	{
		sinal = 'N';
		write (1, &sinal, 1);
	}
	else
	{
		sinal = 'P';
		write (1, &sinal, 1);
	}
}
